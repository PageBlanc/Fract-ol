#include "libft.h"

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	unsigned int	i;
	unsigned int	j;

	i = -1;
	if (little[0] == 0)
		return ((char *)big);
	if (len == 0)
		return (NULL);
	while (big[++i] && i < len)
	{
		j = 0;
		while (big[i + j] == little[j] && little[j] && i + j < len)
			j++;
		if (!little[j])
			return ((char *)&big[i]);
	}
	return (0);
}
